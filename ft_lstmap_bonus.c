#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*new;
	void	*ptr;

	head = NULL;
	while (lst != NULL)
	{
		ptr = f(lst->content);
		new = ft_lstnew(ptr);
		if (new == NULL)
		{
			del(ptr);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, new);
		lst = lst->next;
	}
	return (head);
}
